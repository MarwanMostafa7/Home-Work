void main(){

  ///1-num
  ///هو نوع من data type يستطيع التعامل مع الارقام الصحيحه والعشريه 
  ///ويسمح لنا بتغير قيمه المتغير اي اي نوع من الارقام 
  
  num number = 10;  // int
  print(number);  

  number = 10.5;  // double
  print(number);


  ///2- double
  ///يتعامل فقط مع نةع واحد من الارقام وهي الارقام العشريه 
  
  double number2 = 16.2;  // double
  print(number2);

}